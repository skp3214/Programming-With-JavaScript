// Arithmetic operators

let a=15;
let b=6;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);

// Assignment operators
a+=b;
console.log(a);
a-=b;
console.log(a);

// Comparison operators
let c='a';
let d='b';
console.log(c>d);
console.log(c<d);
console.log(c<=d);
console.log(c>=d);
let e=5;
let f='5';
console.log(e==f);
console.log(e===f);

// Logical Operators
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);
console.log(!true)
console.log(true!=false)

// Ternary Operator
let g=10;
let h=20;
console.log(g>h?g:h);
